package FunctionalInterfaces;

public class PurchaseFunctional {
    private String name;
    private int price;
    private int qty;

    public PurchaseFunctional(String name, int price, int qty) {
        this.name = name;
        this.price = price;
        this.qty = qty;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }

    public int getQty() {
        return qty;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public void setQty(int qty) {
        this.qty = qty;
    }

    @Override
    public String toString() {
        return name + ";" + price + ";" + qty + ";";
    }


}
